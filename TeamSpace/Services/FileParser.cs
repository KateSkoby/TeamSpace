using System;
using System.Collections.Generic;
using System.Globalization;
using System.IO;
using System.Linq;
using System.Text;
using TeamSpace.Models;

namespace TeamSpace.Services
{
    /// Общий сервис для чтения и преобразования данных из CSV-файлов.

    /// Сервис используется в двух модулях приложения:
    /// - модуле анализа состояния дорог по регионам;
    /// - модуле анализа потребительских расходов.

    /// В классе выполняются следующие операции:
    /// - чтение содержимого CSV-файла;
    /// - проверка существования файла и наличия строк с данными;
    /// - автоматическое определение разделителя;
    /// - разбор строк с учётом значений в кавычках;
    /// - преобразование текстовых данных в числовые значения;
    /// - проверка обязательных столбцов для файла расходов;
    /// - формирование списков объектов моделей приложения.

    /// Класс объявлен статическим, поскольку для загрузки данных
    /// не требуется создание экземпляра сервиса или хранение
    /// внутреннего состояния между вызовами методов.
    public static class FileParser
    {
        // ------------------------------------------------------------
        // Загрузка данных для модуля анализа состояния дорог
        // ------------------------------------------------------------

        /// Загружает данные о состоянии дорог из CSV-файла
        /// и преобразует строки файла в список объектов RoadData.

        /// Ожидается, что файл содержит как минимум три столбца:
        /// - год наблюдения;
        /// - наименование региона;
        /// - процент плохих дорог.

        /// Первая строка файла рассматривается как строка заголовков
        /// и не преобразуется в объект модели.

        /// После загрузки полученные записи сортируются
        /// по году по возрастанию.
        public static List<RoadData> LoadRoadDataFromCsv(string filePath)
        {
            // Выполняется чтение всех непустых строк CSV-файла.

            // Метод ReadLines одновременно проверяет:
            // - наличие переданного пути;
            // - существование файла;
            // - наличие заголовка и хотя бы одной строки данных.
            string[] lines = ReadLines(filePath);

            // По строке заголовков определяется символ,
            // которым в текущем CSV-файле разделяются значения.

            // Поддерживаются наиболее распространённые варианты:
            // точка с запятой и запятая.
            char separator = DetectSeparator(lines[0]);

            // Подготавливается коллекция, в которую будут
            // последовательно добавляться считанные записи о дорогах.
            List<RoadData> result = new List<RoadData>();

            // Обработка начинается со второй строки файла,
            // поскольку первая строка содержит названия столбцов.
            for (int lineIndex = 1; lineIndex < lines.Length; lineIndex++)
            {
                // Текущая строка CSV разбивается на отдельные значения.

                // Метод ParseCsvLine позволяет корректно обработать
                // значения, заключённые в кавычки и содержащие
                // символ используемого разделителя.

                // После разбора из каждого значения удаляются
                // пробелы в начале и в конце строки.
                string[] cells = ParseCsvLine(lines[lineIndex], separator)
                    .Select(cell => cell.Trim())
                    .ToArray();

                // Для формирования объекта RoadData требуется
                // наличие не менее трёх значений:
                // года, региона и процента плохих дорог.

                // При недостаточном числе столбцов строка считается
                // повреждённой, поэтому дальнейшая загрузка прекращается
                // с формированием информативного исключения.
                if (cells.Length < 3)
                {
                    throw new InvalidDataException(
                        "Недостаточно значений в строке " +
                        (lineIndex + 1) + " файла с данными о дорогах."
                    );
                }

                // Из значений текущей строки создаётся объект модели RoadData.

                // Значения года и процента преобразуются из текста
                // в числовые типы через специальные методы проверки.
                // Наименование региона сохраняется как строковое значение.
                result.Add(new RoadData
                {
                    Year = ParseInteger(
                        cells[0],
                        lineIndex + 1,
                        "Year"
                    ),

                    Region = cells[1],

                    BadRoadsPercent = ParseDouble(
                        cells[2],
                        lineIndex + 1,
                        "BadRoadsPercent"
                    )
                });
            }

            // Загруженные записи сортируются по году по возрастанию.

            // Такая сортировка обеспечивает правильную хронологическую
            // последовательность при последующем анализе,
            // расчёте статистики и построении графиков.
            return result
                .OrderBy(item => item.Year)
                .ToList();
        }

        // ------------------------------------------------------------
        // Загрузка данных для модуля потребительских расходов
        // ------------------------------------------------------------

        /// Загружает данные о потребительских расходах из CSV-файла
        /// и преобразует строки файла в список объектов ConsumerExpenseData.

        /// Для успешной загрузки в файле должны присутствовать
        /// обязательные столбцы:
        /// - Год;
        /// - Общие расходы;
        /// - Продукты питания;
        /// - Одежда и обувь;
        /// - Жилищные услуги;
        /// - Транспорт;
        /// - Здравоохранение;
        /// - Образование.

        /// Порядок столбцов в файле может отличаться,
        /// поскольку необходимые значения определяются
        /// по названиям заголовков.

        /// После загрузки данные сортируются по году по возрастанию.
        public static List<ConsumerExpenseData> LoadConsumerExpensesFromCsv(
            string filePath)
        {
            // Выполняется чтение CSV-файла и базовая проверка
            // возможности дальнейшей обработки его содержимого.
            string[] lines = ReadLines(filePath);

            // Определяется символ-разделитель, применённый в файле.
            // Проверка выполняется по первой строке с заголовками.
            char separator = DetectSeparator(lines[0]);

            // Из первой строки извлекаются названия столбцов.

            // В начале строки дополнительно удаляется специальный
            // символ BOM, который может автоматически добавляться
            // в UTF-8 файл некоторыми программами.

            // Удаление BOM необходимо для корректного распознавания
            // первого заголовка, например столбца "Год".
            string[] headers = ParseCsvLine(
                    lines[0].TrimStart('\uFEFF'),
                    separator
                )
                .Select(header => header.Trim())
                .ToArray();

            // На основании полученных заголовков формируется словарь,
            // связывающий название каждого необходимого столбца
            // с его фактической позицией в строке CSV-файла.

            // В этом же методе выполняется проверка наличия
            // всех обязательных столбцов для данных расходов.
            Dictionary<string, int> columnIndexes =
                GetConsumerExpenseColumnIndexes(headers);

            // Подготавливается список, в который будут добавлены
            // объекты с годовыми значениями потребительских расходов.
            List<ConsumerExpenseData> result =
                new List<ConsumerExpenseData>();

            // Каждая строка после заголовка преобразуется
            // в отдельный объект модели ConsumerExpenseData.
            for (int lineIndex = 1; lineIndex < lines.Length; lineIndex++)
            {
                // Текущая строка разбивается на значения
                // с учётом выбранного разделителя и возможных кавычек.

                // Из каждой полученной ячейки удаляются лишние
                // пробелы в начале и конце текста.
                string[] cells = ParseCsvLine(lines[lineIndex], separator)
                    .Select(cell => cell.Trim())
                    .ToArray();

                // Количество значений в текущей строке не должно быть
                // меньше количества столбцов, объявленных в заголовке.

                // При несоответствии строка считается неполной,
                // поскольку из неё невозможно безопасно получить
                // все необходимые показатели расходов.
                if (cells.Length < headers.Length)
                {
                    throw new InvalidDataException(
                        "Недостаточно значений в строке " +
                        (lineIndex + 1) + " файла расходов."
                    );
                }

                // Формируется объект модели данных о расходах.

                // Каждое значение выбирается не по фиксированной позиции,
                // а по индексу, ранее найденному по названию столбца.
                // Благодаря этому допускается изменение порядка столбцов
                // в исходном CSV-файле.
                result.Add(new ConsumerExpenseData
                {
                    // Выполняется чтение и преобразование года наблюдения.
                    Year = ParseInteger(
                        cells[columnIndexes["Год"]],
                        lineIndex + 1,
                        "Год"
                    ),

                    // Выполняется чтение показателя общих расходов.
                    TotalExpenses = ParseDecimal(
                        cells[columnIndexes[ConsumerExpenseData.TotalIndicator]],
                        lineIndex + 1,
                        ConsumerExpenseData.TotalIndicator
                    ),

                    // Выполняется чтение расходов на продукты питания.
                    FoodExpenses = ParseDecimal(
                        cells[columnIndexes[ConsumerExpenseData.FoodIndicator]],
                        lineIndex + 1,
                        ConsumerExpenseData.FoodIndicator
                    ),

                    // Выполняется чтение расходов на одежду и обувь.
                    ClothingAndFootwearExpenses = ParseDecimal(
                        cells[columnIndexes[ConsumerExpenseData.ClothingIndicator]],
                        lineIndex + 1,
                        ConsumerExpenseData.ClothingIndicator
                    ),

                    // Выполняется чтение расходов на жилищные услуги.
                    HousingExpenses = ParseDecimal(
                        cells[columnIndexes[ConsumerExpenseData.HousingIndicator]],
                        lineIndex + 1,
                        ConsumerExpenseData.HousingIndicator
                    ),

                    // Выполняется чтение расходов на транспорт.
                    TransportExpenses = ParseDecimal(
                        cells[columnIndexes[ConsumerExpenseData.TransportIndicator]],
                        lineIndex + 1,
                        ConsumerExpenseData.TransportIndicator
                    ),

                    // Выполняется чтение расходов на здравоохранение.
                    HealthcareExpenses = ParseDecimal(
                        cells[columnIndexes[ConsumerExpenseData.HealthcareIndicator]],
                        lineIndex + 1,
                        ConsumerExpenseData.HealthcareIndicator
                    ),

                    // Выполняется чтение расходов на образование.
                    EducationExpenses = ParseDecimal(
                        cells[columnIndexes[ConsumerExpenseData.EducationIndicator]],
                        lineIndex + 1,
                        ConsumerExpenseData.EducationIndicator
                    )
                });
            }

            // Результирующие записи сортируются по году наблюдения.

            // Хронологический порядок требуется для корректного
            // построения графика, вычисления статистики
            // и выполнения прогнозирования.
            return result
                .OrderBy(item => item.Year)
                .ToList();
        }

        // ------------------------------------------------------------
        // Чтение и первоначальная проверка CSV-файла
        // ------------------------------------------------------------

        /// Считывает строки CSV-файла и выполняет базовые проверки
        /// перед дальнейшим разбором его содержимого.

        /// Пустые строки исключаются из результата,
        /// поскольку они не содержат полезных данных
        /// и не должны преобразовываться в объекты моделей.
        private static string[] ReadLines(string filePath)
        {
            // Проверяется наличие пути к файлу.

            // Пустой путь не позволяет определить источник данных,
            // поэтому обработка прекращается до обращения к файловой системе.
            if (string.IsNullOrWhiteSpace(filePath))
            {
                throw new ArgumentException(
                    "Не указан путь к CSV-файлу."
                );
            }

            // Проверяется физическое наличие CSV-файла
            // по переданному пути.

            // При отсутствии файла формируется исключение,
            // содержащее исходный путь для упрощения поиска ошибки.
            if (!File.Exists(filePath))
            {
                throw new FileNotFoundException(
                    "CSV-файл не найден.",
                    filePath
                );
            }

            // Все строки файла считываются в кодировке UTF-8.

            // После чтения из массива удаляются полностью пустые строки
            // и строки, состоящие только из пробельных символов.
            string[] lines = File
                .ReadAllLines(filePath, Encoding.UTF8)
                .Where(line => !string.IsNullOrWhiteSpace(line))
                .ToArray();

            // Минимально допустимая структура CSV-файла включает:
            // - одну строку с заголовками;
            // - хотя бы одну строку с данными.

            // Если строк меньше двух, файл не может быть использован
            // для формирования набора моделей.
            if (lines.Length < 2)
            {
                throw new InvalidDataException(
                    "CSV-файл не содержит строк с данными."
                );
            }

            // Возвращается очищенный массив строк,
            // готовый к дальнейшему разбору.
            return lines;
        }

        // ------------------------------------------------------------
        // Проверка столбцов файла потребительских расходов
        // ------------------------------------------------------------

        /// Формирует словарь индексов столбцов CSV-файла расходов
        /// и проверяет наличие всех обязательных заголовков.

        /// Использование словаря позволяет получать данные по названию
        /// столбца, а не по заранее заданной позиции.
        /// Благодаря этому порядок столбцов в CSV-файле
        /// может быть изменён без изменения логики загрузки.
        private static Dictionary<string, int> GetConsumerExpenseColumnIndexes(
            string[] headers)
        {
            // Задаётся полный перечень столбцов,
            // необходимых для загрузки потребительских расходов.

            // Названия показателей берутся из констант модели,
            // что исключает дублирование строковых значений
            // в разных частях проекта.
            string[] requiredColumns =
            {
                "Год",
                ConsumerExpenseData.TotalIndicator,
                ConsumerExpenseData.FoodIndicator,
                ConsumerExpenseData.ClothingIndicator,
                ConsumerExpenseData.HousingIndicator,
                ConsumerExpenseData.TransportIndicator,
                ConsumerExpenseData.HealthcareIndicator,
                ConsumerExpenseData.EducationIndicator
            };

            // Из массива заголовков формируется словарь:
            // название столбца -> позиция столбца в CSV-строке.

            // Например, если "Транспорт" расположен четвёртым,
            // в словаре для него будет сохранён соответствующий индекс.
            Dictionary<string, int> result =
                headers
                    .Select((header, index) => new { header, index })
                    .ToDictionary(item => item.header, item => item.index);

            // Последовательно проверяется наличие
            // каждого обязательного столбца в сформированном словаре.
            foreach (string requiredColumn in requiredColumns)
            {
                // При отсутствии хотя бы одного показателя
                // невозможно полностью сформировать объект расходов,
                // поэтому загрузка завершается исключением
                // с указанием отсутствующего столбца.
                if (!result.ContainsKey(requiredColumn))
                {
                    throw new InvalidDataException(
                        "В CSV-файле отсутствует обязательный столбец: " +
                        requiredColumn
                    );
                }
            }

            // Возвращается словарь проверенных индексов столбцов.
            return result;
        }

        // ------------------------------------------------------------
        // Определение символа-разделителя CSV-файла
        // ------------------------------------------------------------

        /// Определяет разделитель значений CSV-файла
        /// на основании строки заголовков.

        /// Поддерживаются два распространённых формата:
        /// - разделение значений точкой с запятой;
        /// - разделение значений запятой.
        private static char DetectSeparator(string headerLine)
        {
            // В первую очередь проверяется наличие точки с запятой.

            // Такой разделитель часто используется в русскоязычных
            // CSV-файлах, поскольку запятая может применяться
            // как десятичный разделитель в числовых значениях.
            if (headerLine.Contains(";"))
                return ';';

            // Если точка с запятой отсутствует,
            // выполняется проверка варианта с запятой.
            if (headerLine.Contains(","))
                return ',';

            // При отсутствии обоих поддерживаемых символов
            // невозможно корректно разделить строку на столбцы.
            throw new InvalidDataException(
                "Не удалось определить разделитель CSV-файла."
            );
        }

        // ------------------------------------------------------------
        // Разбор отдельной CSV-строки
        // ------------------------------------------------------------

        /// Разделяет одну строку CSV-файла на отдельные значения
        /// с учётом кавычек и экранированных символов.

        /// Метод позволяет корректно обработать значения,
        /// содержащие сам символ-разделитель внутри кавычек.

        /// Например, строковое значение "Москва, область"
        /// не будет ошибочно разделено на две отдельные ячейки,
        /// если оно заключено в кавычки.
        private static List<string> ParseCsvLine(string line, char separator)
        {
            // Подготавливается коллекция для сохранения
            // всех найденных значений текущей строки.
            List<string> cells = new List<string>();

            // Создаётся изменяемая строка для накопления символов
            // текущей обрабатываемой ячейки.
            StringBuilder currentCell = new StringBuilder();

            // Устанавливается признак нахождения внутри кавычек.

            // Значение false означает, что очередной разделитель
            // должен завершить текущую ячейку.
            // Значение true означает, что разделитель является
            // частью содержимого текстового значения.
            bool insideQuotes = false;

            // Выполняется последовательный просмотр каждого символа
            // исходной CSV-строки.
            for (int index = 0; index < line.Length; index++)
            {
                // Извлекается текущий символ строки.
                char character = line[index];

                // Проверяется, является ли текущий символ кавычкой.
                if (character == '"')
                {
                    // Если внутри значения встречаются две кавычки подряд,
                    // они рассматриваются как одна обычная кавычка
                    // в составе содержимого ячейки.

                    // Такая запись соответствует стандартному способу
                    // экранирования кавычек в CSV-файлах.
                    if (insideQuotes &&
                        index + 1 < line.Length &&
                        line[index + 1] == '"')
                    {
                        currentCell.Append('"');

                        // Вторая кавычка уже была обработана
                        // как часть экранированной пары,
                        // поэтому её отдельная обработка пропускается.
                        index++;
                    }
                    else
                    {
                        // Одиночная кавычка переключает состояние:
                        // начинается или завершается текстовое значение,
                        // заключённое в кавычки.
                        insideQuotes = !insideQuotes;
                    }
                }
                // Разделитель завершает текущую ячейку только в случае,
                // когда он расположен вне кавычек.
                else if (character == separator && !insideQuotes)
                {
                    // Сформированное значение добавляется
                    // в итоговый список ячеек.
                    cells.Add(currentCell.ToString());

                    // Буфер очищается для накопления текста
                    // следующего столбца строки.
                    currentCell.Clear();
                }
                else
                {
                    // Все остальные символы добавляются
                    // к содержимому текущей ячейки.
                    currentCell.Append(character);
                }
            }

            // После завершения цикла в буфере остаётся значение
            // последнего столбца, поскольку после него
            // разделитель уже отсутствует.
            cells.Add(currentCell.ToString());

            // Возвращается список всех значений текущей CSV-строки.
            return cells;
        }

        // ------------------------------------------------------------
        // Преобразование строкового значения в целое число
        // ------------------------------------------------------------

        /// Преобразует текстовое значение в целое число.

        /// Метод используется для чтения года наблюдения
        /// из CSV-файлов обоих модулей приложения.
        private static int ParseInteger(
            string text,
            int lineNumber,
            string columnName)
        {
            // Подготавливается переменная,
            // в которую будет помещён результат успешного преобразования.
            int value;

            // Выполняется попытка преобразовать текст в целое число.

            // Перед преобразованием удаляются лишние пробелы
            // в начале и конце значения.
            if (int.TryParse(text.Trim(), out value))
                return value;

            // Если преобразование выполнить невозможно,
            // формируется стандартное исключение
            // с указанием строки, столбца и исходного значения.
            throw CreateValueException(lineNumber, columnName, text);
        }

        // ------------------------------------------------------------
        // Преобразование строкового значения в decimal
        // ------------------------------------------------------------

        /// Преобразует текстовое значение в число типа decimal.

        /// Метод используется для денежных показателей
        /// в модуле анализа потребительских расходов.

        /// Поддерживаются числовые значения:
        /// - с запятой в качестве десятичного разделителя;
        /// - с точкой в качестве десятичного разделителя;
        /// - с обычными или неразрывными пробелами
        ///   в качестве разделителей разрядов.
        private static decimal ParseDecimal(
            string text,
            int lineNumber,
            string columnName)
        {
            // Подготавливается переменная для хранения
            // успешно преобразованного денежного значения.
            decimal value;

            // Из текстового представления удаляются пробелы,
            // которые могут использоваться как разделители разрядов.
            string preparedText = PrepareNumber(text);

            // Сначала выполняется попытка преобразования
            // с использованием русской культуры.

            // Такой вариант поддерживает значения,
            // в которых дробная часть отделена запятой,
            // например: 1250,75.
            if (decimal.TryParse(
                preparedText,
                NumberStyles.Any,
                CultureInfo.GetCultureInfo("ru-RU"),
                out value))
            {
                return value;
            }

            // Если преобразование в русском формате не удалось,
            // выполняется попытка обработки инвариантного формата.

            // Такой вариант поддерживает значения с точкой
            // в качестве десятичного разделителя,
            // например: 1250.75.
            if (decimal.TryParse(
                preparedText,
                NumberStyles.Any,
                CultureInfo.InvariantCulture,
                out value))
            {
                return value;
            }

            // При невозможности преобразования создаётся исключение
            // с точным указанием местоположения некорректного значения.
            throw CreateValueException(lineNumber, columnName, text);
        }

        // ------------------------------------------------------------
        // Преобразование строкового значения в double
        // ------------------------------------------------------------

        /// Преобразует текстовое значение в число типа double.

        /// Метод применяется при чтении процентных показателей
        /// состояния дорог.

        /// Поддерживаются значения как с точкой,
        /// так и с запятой в качестве десятичного разделителя.
        private static double ParseDouble(
            string text,
            int lineNumber,
            string columnName)
        {
            // Подготавливается переменная для хранения
            // результата успешного преобразования.
            double value;

            // Из текста удаляются пробелы,
            // способные мешать корректному распознаванию числа.
            string preparedText = PrepareNumber(text);

            // Сначала выполняется преобразование
            // с использованием инвариантной культуры.

            // Такой формат ожидает точку
            // в качестве десятичного разделителя,
            // например: 14.75.
            if (double.TryParse(
                preparedText,
                NumberStyles.Any,
                CultureInfo.InvariantCulture,
                out value))
            {
                return value;
            }

            // При неудаче выполняется дополнительная попытка
            // преобразования с использованием русской культуры.

            // Такой формат позволяет обработать значения
            // с запятой в качестве десятичного разделителя,
            // например: 14,75.
            if (double.TryParse(
                preparedText,
                NumberStyles.Any,
                CultureInfo.GetCultureInfo("ru-RU"),
                out value))
            {
                return value;
            }

            // Если значение невозможно распознать
            // ни в одном из поддерживаемых форматов,
            // формируется исключение с описанием ошибки.
            throw CreateValueException(lineNumber, columnName, text);
        }

        // ------------------------------------------------------------
        // Подготовка текстового числового значения
        // ------------------------------------------------------------

        /// Подготавливает текстовое числовое значение
        /// перед преобразованием в числовой тип.

        /// Из строки удаляются:
        /// - неразрывные пробелы;
        /// - обычные пробелы;
        /// - пробелы в начале и конце значения.

        /// Такая обработка позволяет корректно читать числа,
        /// записанные с визуальным разделением разрядов,
        /// например "1 250 000" или "1 250 000".
        private static string PrepareNumber(string text)
        {
            // Удаляются различные варианты пробелов,
            // встречающиеся внутри числовых значений CSV-файла.
            return text
                .Replace("\u00A0", string.Empty)
                .Replace(" ", string.Empty)
                .Trim();
        }

        // ------------------------------------------------------------
        // Формирование исключения при ошибке числового значения
        // ------------------------------------------------------------

        /// Создаёт стандартное исключение для ситуации,
        /// когда значение из CSV-файла невозможно
        /// преобразовать в требуемый числовой тип.

        /// В сообщение включаются:
        /// - номер строки;
        /// - название столбца;
        /// - исходное некорректное значение.

        /// Единый метод формирования ошибки обеспечивает
        /// одинаковый формат сообщений во всех операциях парсинга.
        private static InvalidDataException CreateValueException(
            int lineNumber,
            string columnName,
            string text)
        {
            // Формируется исключение с сообщением,
            // позволяющим быстро определить расположение
            // повреждённого или неверно заполненного значения в CSV-файле.
            return new InvalidDataException(
                "Некорректное значение в строке " + lineNumber +
                ", столбец \"" + columnName + "\": " + text
            );
        }
    }
}