using System.Collections.Generic;
using System.Linq;
using TeamSpace.Models;

namespace TeamSpace.Services
{
    /// Общий сервис для выполнения статистических расчётов.

    /// В классе объединены методы для двух модулей приложения:
    /// 1. Анализ состояния дорог по регионам.
    /// 2. Анализ потребительских расходов по годам.

    /// Все методы объявлены статическими, поскольку для выполнения
    /// расчётов не требуется создание экземпляра класса и хранение
    /// внутреннего состояния между вызовами.
    public static class StatisticsCalculator
    {
        // ------------------------------------------------------------
        // Статистика для модуля анализа состояния дорог
        // ------------------------------------------------------------

        /// Определяет регионы с максимальным и минимальным снижением
        /// процента плохих дорог за рассматриваемый период.

        /// Для каждого региона сравниваются значения первого и последнего
        /// годов после сортировки данных по возрастанию года.

        /// Положительная разница означает снижение процента плохих дорог,
        /// то есть улучшение показателя в регионе.

        /// Кортеж из четырёх значений:
        /// - регион с максимальным снижением;
        /// - значение максимального снижения;
        /// - регион с минимальным положительным снижением;
        /// - значение минимального положительного снижения.

        /// При отсутствии подходящих данных возвращаются значения
        /// "Нет данных" и нулевые показатели.

        public static (
            string MaxDecreaseRegion,
            double MaxDecrease,
            string MinDecreaseRegion,
            double MinDecrease)
            FindMaxMinDecrease(List<RoadData> data)
        {
            // При отсутствии входных данных выполнение дальнейших
            // вычислений невозможно, поэтому возвращается пустой результат.
            if (data == null || data.Count == 0)
                return ("Нет данных", 0, "Нет данных", 0);

            // Из общего набора данных формируется список уникальных регионов.
            // Повторяющиеся наименования исключаются, поскольку каждый
            // регион должен быть обработан только один раз.
            IEnumerable<string> regions = data
                .Select(item => item.Region)
                .Distinct();

            // Создаётся список для хранения регионов, в которых было
            // зафиксировано снижение процента плохих дорог.
            // В каждом элементе сохраняются наименование региона
            // и рассчитанная величина снижения.
            List<(string Region, double Decrease)> decreases =
                new List<(string Region, double Decrease)>();

            // Последовательно выполняется анализ данных каждого региона.
            foreach (string region in regions)
            {
                // Для текущего региона выбираются только относящиеся к нему
                // записи, после чего они сортируются по году.
                // Сортировка необходима для корректного определения
                // начального и конечного значений показателя.
                List<RoadData> regionData = data
                    .Where(item => item.Region == region)
                    .OrderBy(item => item.Year)
                    .ToList();

                // Для сравнения изменения показателя требуется минимум
                // два периода наблюдений. Регион с одной записью
                // в расчётах изменения не участвует.
                if (regionData.Count < 2)
                    continue;

                // Снижение определяется как разница между процентом
                // плохих дорог в первом и последнем доступных годах.

                // Пример:
                // было 35 %, стало 20 %;
                // снижение = 35 - 20 = 15 процентных пунктов.
                double decrease =
                    regionData.First().BadRoadsPercent -
                    regionData.Last().BadRoadsPercent;

                // В итоговый набор включаются только регионы,
                // в которых показатель действительно уменьшился.
                // Нулевое значение означает отсутствие изменения,
                // отрицательное значение означает ухудшение ситуации.
                if (decrease > 0)
                    decreases.Add((region, decrease));
            }

            // Если ни в одном регионе не обнаружено положительное снижение,
            // информация о максимальном и минимальном улучшении отсутствует.
            if (decreases.Count == 0)
                return ("Нет данных", 0, "Нет данных", 0);

            // Из списка улучшений выбирается запись с наибольшей
            // величиной снижения процента плохих дорог.
            var maximum = decreases
                .OrderByDescending(item => item.Decrease)
                .First();

            // Из списка улучшений выбирается запись с наименьшей
            // положительной величиной снижения процента плохих дорог.
            var minimum = decreases
                .OrderBy(item => item.Decrease)
                .First();

            // Возвращаются названия найденных регионов
            // и соответствующие значения снижения показателя.
            return (
                maximum.Region,
                maximum.Decrease,
                minimum.Region,
                minimum.Decrease
            );
        }

        /// Возвращает записи о состоянии дорог только для указанного региона.

        /// Результат упорядочивается по году по возрастанию, что позволяет
        /// использовать его для построения графиков и последовательного
        /// анализа динамики показателя.
        public static List<RoadData> GetDataByRegion(
            List<RoadData> allData,
            string region)
        {
            // При отсутствии полного набора данных возвращается
            // пустая коллекция, пригодная для безопасной привязки
            // к элементам интерфейса.
            if (allData == null)
                return new List<RoadData>();

            // Из общего списка выбираются записи указанного региона.
            // Результат сортируется по году для сохранения
            // хронологического порядка наблюдений.
            return allData
                .Where(item => item.Region == region)
                .OrderBy(item => item.Year)
                .ToList();
        }

        /// Формирует список всех регионов, присутствующих
        /// в наборе данных о состоянии дорог.

        /// Повторяющиеся названия исключаются, а итоговый список
        /// сортируется в алфавитном порядке.
        public static List<string> GetAllRegions(List<RoadData> data)
        {
            // При отсутствии входного набора данных формируется
            // пустой список регионов.
            if (data == null)
                return new List<string>();

            // Из каждой записи извлекается название региона.
            // Затем исключаются повторения и выполняется
            // сортировка по алфавиту.
            return data
                .Select(item => item.Region)
                .Distinct()
                .OrderBy(region => region)
                .ToList();
        }

        // ------------------------------------------------------------
        // Статистика для модуля анализа потребительских расходов
        // ------------------------------------------------------------

        /// Рассчитывает статистические показатели для выбранной категории
        /// потребительских расходов.

        /// Для каждого года, начиная со второго, определяется процентное
        /// изменение значения относительно предыдущего года.

        /// На основании рассчитанных изменений возвращаются:
        /// - количество доступных периодов;
        /// - максимальное процентное изменение;
        /// - минимальное процентное изменение.
        public static ConsumerExpenseStatistics CalculateExpenseStatistics(
            List<ConsumerExpenseData> data,
            string indicatorName)
        {
            // Создаётся объект результата со значениями по умолчанию.
            // Этот объект будет возвращён также в случаях, когда
            // вычисление статистики невозможно.
            ConsumerExpenseStatistics result =
                new ConsumerExpenseStatistics();

            // При отсутствии записей отсутствуют периоды
            // и данные для расчёта статистики.
            if (data == null || data.Count == 0)
                return result;

            // Записи сортируются по году по возрастанию.
            // Хронологическая последовательность обязательна,
            // поскольку каждое значение сравнивается с предыдущим годом.
            List<ConsumerExpenseData> orderedData = data
                .OrderBy(item => item.Year)
                .ToList();

            // Количество загруженных строк сохраняется как количество
            // доступных периодов наблюдений.
            result.PeriodCount = orderedData.Count;

            // При наличии только одного периода невозможно вычислить
            // изменение относительно предыдущего года.
            if (orderedData.Count < 2)
                return result;

            // Создаётся коллекция для хранения процентных изменений
            // выбранного показателя между соседними годами.
            List<decimal> percentageChanges = new List<decimal>();

            // Последовательно обрабатываются пары соседних периодов:
            // предыдущий год и текущий год.
            for (int index = 1; index < orderedData.Count; index++)
            {
                // Из предыдущей записи извлекается значение
                // выбранного пользователем показателя расходов.
                decimal previousValue =
                    orderedData[index - 1].GetIndicatorValue(indicatorName);

                // Из текущей записи извлекается значение
                // того же выбранного показателя расходов.
                decimal currentValue =
                    orderedData[index].GetIndicatorValue(indicatorName);

                // Процентное изменение относительно нулевого значения
                // определить невозможно из-за деления на ноль.
                // Такая пара периодов исключается из расчёта.
                if (previousValue == 0)
                    continue;

                // Рассчитывается относительное изменение показателя
                // между соседними годами в процентах.

                // Формула:
                // изменение = (текущее значение - предыдущее значение)
                //             / предыдущее значение * 100.

                // Положительное значение показывает рост расходов,
                // отрицательное значение показывает их снижение.
                decimal change =
                    (currentValue - previousValue) /
                    previousValue * 100M;

                // Рассчитанное изменение добавляется в коллекцию
                // для последующего поиска максимального и минимального
                // значений.
                percentageChanges.Add(change);
            }

            // Если все пары периодов были исключены из расчёта
            // или корректные изменения отсутствуют, возвращается
            // результат только с количеством периодов.
            if (percentageChanges.Count == 0)
                return result;

            // Определяется наибольшее процентное изменение
            // выбранного показателя за весь рассматриваемый период.
            result.MaximumPercentageChange = percentageChanges.Max();

            // Определяется наименьшее процентное изменение
            // выбранного показателя за весь рассматриваемый период.
            result.MinimumPercentageChange = percentageChanges.Min();

            // Возвращается заполненный объект статистики,
            // предназначенный для отображения на форме.
            return result;
        }
    }
}