using System;
using System.Collections.Generic;
using System.Linq;

namespace TeamSpace.Services
{
    /// Общий сервис прогнозирования временных рядов
    /// методом скользящей средней.
    /// 
    /// Сервис используется в двух модулях приложения:
    /// - методы, работающие с типом double, предназначены
    ///   для анализа данных о состоянии дорог;
    /// - методы, работающие с типом decimal, предназначены
    ///   для анализа потребительских расходов.

    /// Класс объявлен статическим, поскольку расчёты выполняются
    /// только на основании переданных данных и не требуют хранения
    /// состояния между вызовами методов.
    public static class MovingAverageForecast
    {
        // ------------------------------------------------------------
        // Построение прогноза для данных типа double
        // ------------------------------------------------------------

        /// Рассчитывает прогноз на указанное количество будущих периодов
        /// методом скользящей средней для данных типа double.

        /// Метод предназначен для показателей, представленных
        /// вещественными числами, например для процентов плохих дорог.

        /// При расчёте каждого нового прогнозного значения используется
        /// среднее арифметическое последних значений, количество которых
        /// определяется параметром period.

        /// Каждое рассчитанное прогнозное значение добавляется
        /// во временный рабочий список и участвует в вычислении
        /// последующих прогнозных значений.
        public static List<double> CalculateForecast(
            List<double> historicalData,
            int period,
            int forecastSteps)
        {
            // Выполняется проверка корректности всех параметров,
            // необходимых для построения прогноза.

            // При отсутствии списка данных в метод проверки передаётся 0,
            // что приводит к формированию понятного исключения
            // о недостаточном количестве исходных значений.
            ValidateParameters(
                historicalData == null ? 0 : historicalData.Count,
                period,
                forecastSteps
            );

            // Создаётся рабочая копия исторических данных.

            // Копия необходима, поскольку в процессе прогнозирования
            // рассчитанные значения будут последовательно добавляться
            // в набор данных для вычисления следующих периодов.

            // Исходный список historicalData при этом не изменяется.
            List<double> workingData =
                new List<double>(historicalData);

            // Создаётся список для накопления только прогнозных значений.
            // Именно этот список будет возвращён после завершения расчёта.
            List<double> forecast = new List<double>();

            // Выполняется последовательный расчёт каждого будущего периода.
            // Количество итераций соответствует количеству необходимых
            // прогнозных шагов.
            for (int step = 0; step < forecastSteps; step++)
            {
                // Из рабочего списка выбираются последние значения,
                // количество которых равно размеру окна скользящей средней.

                // Например, при period = 3 для расчёта используются
                // три последних известных или ранее спрогнозированных
                // значения.

                // После выбора значений рассчитывается их среднее
                // арифметическое, которое принимается за прогноз
                // следующего периода.
                double nextValue = workingData
                    .Skip(workingData.Count - period)
                    .Take(period)
                    .Average();

                // Рассчитанное значение сохраняется в итоговом списке
                // прогнозов для последующего возврата вызывающему коду.
                forecast.Add(nextValue);

                // Рассчитанное значение добавляется в рабочий ряд.

                // Благодаря этому при построении прогноза более чем
                // на один период вперёд последующие расчёты учитывают
                // уже полученные прогнозные значения.
                workingData.Add(nextValue);
            }

            // Возвращается сформированная последовательность
            // прогнозных значений.
            return forecast;
        }

        // ------------------------------------------------------------
        // Построение прогноза для денежных данных типа decimal
        // ------------------------------------------------------------

        /// Рассчитывает прогноз на указанное количество будущих периодов
        /// методом скользящей средней для данных типа decimal.

        /// Метод предназначен для расчётов, связанных с денежными
        /// значениями, например с потребительскими расходами.
        /// Использование decimal позволяет снизить погрешности,
        /// возникающие при работе с финансовыми величинами.

        /// При расчёте каждого следующего периода используется среднее
        /// арифметическое последних period значений временного ряда.
        /// Рассчитанное значение включается в рабочий список данных
        /// и используется при построении дальнейшего прогноза.
        public static List<decimal> CalculateForecast(
            List<decimal> historicalData,
            int period,
            int forecastSteps)
        {
            // Проверяется наличие достаточного количества исходных данных,
            // корректность размера окна и корректность количества
            // будущих прогнозных периодов.
            ValidateParameters(
                historicalData == null ? 0 : historicalData.Count,
                period,
                forecastSteps
            );

            // Создаётся копия переданного списка денежных значений.

            // Рабочая копия используется для добавления новых прогнозных
            // значений без изменения исходного набора historicalData.
            List<decimal> workingData =
                new List<decimal>(historicalData);

            // Создаётся список результатов, содержащий значения,
            // которые должны быть отображены на графике
            // или использованы в дальнейших операциях формы.
            List<decimal> forecast = new List<decimal>();

            // Выполняется расчёт заданного количества будущих периодов.
            for (int step = 0; step < forecastSteps; step++)
            {
                // Из рабочего временного ряда выбираются последние
                // period значений и вычисляется их среднее арифметическое.

                // Полученное среднее является расчётным прогнозом
                // для очередного будущего периода.
                decimal nextValue = workingData
                    .Skip(workingData.Count - period)
                    .Take(period)
                    .Average();

                // В итоговый список добавляется прогнозное значение,
                // округлённое до двух знаков после запятой.

                // Такое представление удобно для денежных данных,
                // поскольку соответствует точности до копеек.
                forecast.Add(Math.Round(nextValue, 2));

                // Неокруглённое расчётное значение добавляется
                // во временный рабочий ряд и используется при вычислении
                // следующих прогнозных периодов.

                // Такой порядок сохраняет вычислительную точность
                // в промежуточных операциях.
                workingData.Add(nextValue);
            }

            // Возвращается список денежных прогнозных значений.
            return forecast;
        }

        // ------------------------------------------------------------
        // Расчёт скользящей средней для данных типа double
        // ------------------------------------------------------------

        /// Рассчитывает значения скользящей средней
        /// для исторического ряда типа double.

        /// В отличие от методов CalculateForecast, данный метод
        /// не создаёт значения для будущих периодов.
        /// Он вычисляет сглаженные значения только для уже имеющихся данных.

        /// Первое значение результата появляется только после накопления
        /// количества исходных элементов, равного размеру окна period.
        public static List<double> CalculateMovingAverage(
            List<double> data,
            int period)
        {
            // Выполняется проверка возможности расчёта скользящей средней.

            // Для вычисления хотя бы одного среднего значения
            // в списке должно быть не меньше элементов,
            // чем указано в размере окна period.
            ValidateHistoricalData(
                data == null ? 0 : data.Count,
                period
            );

            // Создаётся коллекция для хранения вычисленных
            // значений сглаженного временного ряда.
            List<double> result = new List<double>();

            // Начальная позиция равна period - 1, поскольку именно
            // к этому моменту в исходных данных становится доступно
            // первое полное окно для усреднения.

            // Например, при period = 3 первое среднее рассчитывается
            // по элементам с индексами 0, 1 и 2.
            for (int index = period - 1; index < data.Count; index++)
            {
                // Для текущей позиции выбирается окно из period элементов:
                // от первого элемента текущего окна
                // до текущего элемента включительно.

                // После этого вычисляется среднее значение
                // выбранного участка временного ряда.
                result.Add(
                    data.Skip(index - period + 1)
                        .Take(period)
                        .Average()
                );
            }

            // Возвращается последовательность рассчитанных
            // значений скользящей средней.
            return result;
        }

        // ------------------------------------------------------------
        // Расчёт скользящей средней для денежных данных типа decimal
        // ------------------------------------------------------------

        /// Рассчитывает значения скользящей средней
        /// для исторического денежного ряда типа decimal.

        /// Метод позволяет получить сглаженную динамику расходов
        /// без построения прогноза на будущие периоды.
        /// Использование decimal обеспечивает корректную работу
        /// с финансовыми значениями.
        public static List<decimal> CalculateMovingAverage(
            List<decimal> data,
            int period)
        {
            // Проверяется корректность размера периода
            // и достаточность исходного набора денежных значений.
            ValidateHistoricalData(
                data == null ? 0 : data.Count,
                period
            );

            // Подготавливается список, в который будут добавлены
            // рассчитанные значения скользящей средней.
            List<decimal> result = new List<decimal>();

            // Последовательно рассматриваются все положения полного окна.

            // Обработка начинается с индекса period - 1,
            // так как ранее отсутствует необходимое количество
            // предыдущих значений для вычисления среднего.
            for (int index = period - 1; index < data.Count; index++)
            {
                // Из исходных данных выбирается текущее окно,
                // состоящее из period последовательных элементов.

                // Среднее значение выбранного окна добавляется
                // в результирующий список.
                result.Add(
                    data.Skip(index - period + 1)
                        .Take(period)
                        .Average()
                );
            }

            // Возвращается рассчитанный сглаженный ряд денежных значений.
            return result;
        }

        // ------------------------------------------------------------
        // Проверка параметров для построения прогноза
        // ------------------------------------------------------------

        /// Проверяет параметры, необходимые для построения прогноза.

        /// Проверка включает:
        /// - допустимость размера окна скользящей средней;
        /// - наличие достаточного количества исходных данных;
        /// - корректность количества будущих прогнозных периодов.
        private static void ValidateParameters(
            int dataCount,
            int period,
            int forecastSteps)
        {
            // Выполняется общая проверка исходных данных
            // и размера окна скользящей средней.
            ValidateHistoricalData(dataCount, period);

            // Количество будущих периодов должно быть положительным,
            // поскольку нулевое или отрицательное значение
            // не позволяет сформировать прогноз.
            if (forecastSteps <= 0)
            {
                throw new ArgumentException(
                    "Количество шагов прогноза должно быть больше 0."
                );
            }
        }

        // ------------------------------------------------------------
        // Проверка исходных данных и размера окна
        // ------------------------------------------------------------

        /// Проверяет параметры исторического ряда,
        /// используемые для вычисления скользящей средней.

        /// Данный метод применяется как при построении прогноза,
        /// так и при расчёте сглаженных исторических значений.
        private static void ValidateHistoricalData(
            int dataCount,
            int period)
        {
            // Размер окна должен быть положительным числом.
            // При нулевом или отрицательном периоде
            // вычисление среднего значения не имеет смысла.
            if (period <= 0)
            {
                throw new ArgumentException(
                    "Период должен быть больше 0."
                );
            }

            // Количество исходных данных должно быть не меньше,
            // чем размер выбранного окна.

            // Например, для периода 3 требуется минимум
            // три исторических значения.
            if (dataCount < period)
            {
                throw new ArgumentException(
                    "Недостаточно данных для периода " + period + "."
                );
            }
        }
    }
}